export function getPoemById(id: string): Poem | undefined {
  return poems.find((p) => p.id === id);
}

export function getPoemsByPeriod(period: string): Poem[] {
  return poems.filter((p) => p.period === period);
}

export function getAllPeriods(): string[] {
  return [...new Set(poems.map((p) => p.period))];
}

export function searchPoems(query: string): Poem[] {
  const q = query.toLowerCase();
  return poems.filter(
    (p) =>
      p.title.toLowerCase().includes(q) ||
      p.content.some((line) => line.toLowerCase().includes(q)) ||
      p.themes.some((t) => t.toLowerCase().includes(q)) ||
      p.commentary.toLowerCase().includes(q)
  );
}
